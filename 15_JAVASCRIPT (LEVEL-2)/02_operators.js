//ARITHMETIC OPERATORS
let a = 10;
let b = 5;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

//UNARY OPERATORS
console.log(a)        //10
console.log(a++)      //10
console.log(++a)      //12
console.log(b)        //5
console.log(b--)      //5
console.log(--b)      //3

//ASSIGNMENT OPERATORS
//   =, +=, -=, *=, /=, %=, etc

//COMPARISON OPERATORS
//   >, >=, <, <=, ==, !=

