let array = ["a", "b", "c", "d"]
console.log(array)
console.log(array.length)

// PUSH
array.push("e")
console.log(array)
console.log(array.length)

// POP
array.pop()
console.log(array)
console.log(array.length)

// UNSHIFT
array.unshift("FIRST")
console.log(array)
console.log(array.length)

//SHIFT
array.shift()
console.log(array)
console.log(array.length)